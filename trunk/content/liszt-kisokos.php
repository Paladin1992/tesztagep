        <p class="intro">
            Ember legyen a talpán, aki a polc előtt állva kiigazodik a számtalan lisztféle között.
            Ebben próbálunk segítséget nyújtani: mit, miért és mihez használjunk?
        </p>

        <img alt="Lisztfélék" src="images/lisztfelek.jpg" class="align-right padding-left"/>

        <p>
            Az emberi táplálkozásban a gabonafélék csoportjába tartozó növények
            (pl. búza, kukorica, rizs, árpa, rozs, zab, tritikále, köles, cirok, pohánka, amarant, tönkölybúza) a legfontosabbak,
            mert szemtermésük energiában gazdag, jól tárolható, viszonylag egyszerűen feldolgozható és sokféleképpen felhasználható.
        </p>
        <p>
            Többségüket már több ezer éve termesztik. A különböző gabonaféléknek eltérő az éghajlat- és talajigénye.
            Hazánk természeti adottságai elsősorban a búza, kukorica, árpa, rozs és zab termesztésének kedvez.
        </p>
        <p>
            A gabonafélék őrlése során lisztet, darát és korpát állítanak elő.
            A liszt a gabonafélék terméséből őrléssel és részleges héjeltávolítással nyert finom szemcséjű termék.
            Tágabb értelemben a gabonaőrleményeken kívül a gumók, hüvelyesek őrleményeit is a lisztek közé soroljuk (pl. burgonyaliszt, borsóliszt).
        </p>

        <p>A lisztek csoportosítása többféle szempont szerint történhet:</p>
        <ul>
            <li>gabona fajtája alapján (búza, rozs, rizs, kukorica)</li>
            <li>szemcseméret alapján (sima liszt, fogós liszt - lásd alább)</li>
            <li>hamutartalom alapján (izzítás és kénsavas kezelés után visszamaradó anyag, amely a színét adja a lisztnek)</li>
        </ul>

        <p>
            A gabonaszem fizikailag is szétválasztható három fő része a héj, a magbelső és a csíra.
            Az összetevők eltérő koncentrációban vannak jelen ezekben.
            A héj a fő cellulóz-, élelmirost-hordozó rész, az alatta lévő rétegek ásványi anyagokban és vitaminokban gazdagok.
            A magbelsőt főleg keményítő és fehérjék alkotják, jelentős a cukor-, zsír- és ásványianyag-tartalma.
            A csíra szintén gazdag ásványi anyagban, A- és E-vitaminban.
        </p>
        <p>
            A hagyományos feldolgozás során a gabonaszem eredeti vitamin- és ásványianyag-tartalmának 20-80%-a,
            rosttartalmának mintegy 90%-a kerül a korpába, de elvész a gabonaszem héja alatt lévő értékes fehérje- és zsírsav-komponensek jelentős része is.
            Ezért táplálkozástani szempontból előnyösebb a gabonaszem szinte összes alkotóját tartalmazó, teljes kiőrlésű lisztek használata.
        </p>


        <h2>Melyiket válasszam?</h2>

        <h3>Búzaliszt (B)</h3>
        <p>
            A búza a legfontosabb, és egyben leggyakrabban használt gabonaféle.
            Az a búzaliszt, amely szinte teljesen csak a magbelsőből áll, minőség szempontjából fontos anyagokat veszít el,
            ezért táplálkozástani szempontból előnyösebb, ha olyan lisztet választunk, amely a búzaszem őrlése után a búza mindhárom részét tartalmazza.
            Ezek a <strong>teljes kiőrlésű lisztek</strong> (BL 200), amelyek tartalmazzák a búzaszem külső rétegeit (korpa) és a csírát is.
            A búzalisztnek három fő típusát különböztethetjük meg: finomliszt, fogós liszt, kenyérliszt.
        </p>
        <p>
            A <strong>finomliszt</strong> (pl. BL 51; BL 55) apró szemcseméretű, alacsony korpatartalmú, világos színárnyalatú őrlemény.
            Főleg cukrászati termékek és péksütemények előállítására használják.
            Háztartásokban pedig sűrítéshez (habaráshoz, rántáshoz, lisztszóráshoz), panírozáshoz, palacsintatésztába,
            valamint kalácsokhoz, kelt tésztákhoz és süteményekhez használják.
        </p>
        <p>
            A <strong>fogós liszt</strong> (rétes- vagy grízes liszt) (pl. BF 50; BFF 55) a finomlisztnél érdesebb tapintású, nagyobb szemcseméretű őrlemény.
            Mivel sikértartalma (a gabonafélék szemtermésében előforduló, vízben nem oldódó fehérjék együttese,
            amelyek főként a tészta rugalmasságát és nyújthatóságát határozzák meg) az őrlés során kevésbé károsodott,
            ezért olyan termékeknél használják, ahol a jó sikérminőség elsőrendű követelmény.
        </p>
        <p>
            A <strong>rétesliszt</strong> pogácsához, piskótához, réteshez, valamint gyúrt tészta, nokedli (galuska) és nudli készítéséhez való.
            A csak lisztbe (vagy paprikás lisztbe) forgatott húsok, halak sütésénél is ezt a lisztfajtát érdemes használni.
            A rétesliszt és a gríz korpatartalma, hamutartalma azonos a finomlisztével, hiszen ehhez is csak a búzamagbelsőt használják fel, csak durvábbra őrlik.
        </p>
        <p>
            A <strong>kenyérlisztek</strong> (pl. BL 80; BL 112; BL 160) viszonylag több héjrészt tartalmazó, sötétebb színű őrlemények.
            A sütőiparban a különböző kenyérfajták alapanyagaiként kerülnek felhasználásra.
            Ez a legideálisabb lisztfajta kenyerek és kelt tészták sütéséhez.
        </p>

        <h3>Rozsliszt (RL)</h3>
        <p>
            A rozs a búza után a legfontosabb gabonafélénk. A rozslisztet (pl. RL 60; RL 90; RL 125; RL 190) leginkább kenyérsütéshez használják.
            Vitalizáló, immunerősítő hatású, növeli a fizikai erőnlétet. Gazdag B-vitaminokban, kalciumban, magnéziumban, nitrogénben és foszforban.
            Alacsony sikértartalma miatt célszerű más lisztekkel keverni.
        </p>

        <h3>Durum simaliszt (DSL)</h3>
        <p>
            A durumbúza a mediterrán vidék búzája. Magas a fehérjetartalma. A durumbúza ­magbelsőjére jellemző ­sárgás alapszínű, finomszemcsés őrlemény.
            Csekély mennyiségben a lisztszemcsékkel azonos méretű világos héjrészeket is tartalmaz.
            A durumbúza lisztje magas fehérjetartalmú, kitűnő tészták készülhetnek belőle.
        </p>
        
        <h3>Graham liszt (GL 200)</h3>
        <p>
            A Graham liszt abban különbözik a teljes kiőrlésű liszttől, hogy a liszt szemcsemérete nagyobb.
            A szervezetbe jutva a szénhidrát, mivel nagy szemcseméretű, lassan szívódik fel,
            illetve ezzel egyidejűleg étkezés után nem alakul ki olyan hamar éhségérzet.
            Nem mellékes a Graham lisztben található búzacsíra-, ásványianyag-, folsav- és B-vitamin-, továbbá kálium-, kalcium-,
            foszfor-, magnézium-, cink- és mangántartalom sem. Jobb a tápértéke, mert magasabb a vitamin-, ásványianyag- és rosttartalma.
        </p>
        
        <h3>Tönkölybúzaliszt (TBL)</h3>
        <p>
            A közönséges búzához képest a tönkölybúza (pl. TBL 70; TBL 300) fehérjetartalma hatszor magasabb.
            Az emberi szervezet számára sokkal jobban hasznosítható fehérjéket tartalmaz, aminosav-összetétele kiváló.
            A tönköly értékeit tovább növeli a közönséges búzához viszonyított magas ásványianyag-tartalma és változatos vitamin-összetétele is.
        </p>
        
        <h3>Kukoricaliszt</h3>
        <p>
            Gluténmentes (A glutén a búzalisztben található fehérje, allergizáló hatású lehet).
            Európa több országában fontos alapanyag, de a kínai konyha is gyakran dolgozik vele.
            Kiváló mártások sűrítéséhez, galuskához, krémlevesekhez, hústöltelékek dúsításához.
            Belőle készül két híres népi étel, a puliszka és a prósza.
        </p>
        <p>
            Kis mennyiségben érdemes a kenyérhez adni, amely a kukoricaliszttől kissé édeskés, kellemes ízű lesz.
            Édes süteményekhez is jól használható. Vigyázni kell, hogy a kukoricaliszt friss legyen, mert míg frissen édeskés ízű,
            kellemes illatú, addig az állott kukoricaliszt könnyen megkeseredik.
        </p>

        <h3>Rizsliszt</h3>
        <p>
            Barna és fehér rizsből is készülhet. Selymes textúrájú, gluténmentes.
            Rizslisztből készül a rizstészta is, ami a kínai ételek gyakori alkotója. Sütéshez rendszerint más lisztekkel keverik.
            A rizsliszt jól használható porhanyós, omlós tésztákhoz, de galuska, palacsinta, krémleves is készülhet belőle.
            Édestésztákhoz is kitűnő, szószokat is sűríthetünk vele.
        </p>
        <p>
            A rizsliszt előnye, hogy sütés során nincs szükség tojásra, így a rizslisztből készült süteményeket tojásallergiában szenvedők
            vagy magas koleszterinszinttel rendelkezők is fogyaszthatják.
        </p>

        <h3>Hajdinaliszt</h3>
        <p>
            Gluténmentes. Sötét és világos változatban kapható. A hajdinaliszt érdes, földes jellegű ízt kölcsönöz a kenyérnek,
            de rosttartalma kétszerese a Graham lisztének. Telítetlen zsírsavtartalma 77%-kal több, mint a búzáé.
            Hajdinalisztből készül az orosz lepény, a blini. Ezenkívül kenyeret, száraztésztát vagy hajdinakását is készíthetünk belőle.
        </p>

        <h3>Burgonyaliszt</h3>
        <p>
            Gluténmentes. Szárított burgonya lereszelésével készítik. Ha sütésre használják, akkor általában más lisztekkel keverik össze.
            Jól használható levesek és szószok sűrítésére, illetve burgonyás kenyér sütéséhez.
        </p>

        <h3>Étkezési búzadara</h3>
        <p>
            A búzamag belsőjének nagy szemcsés őrleménye. Fehéres krémszínű, kismértékben a daraszemcsére tapadó héjat is tartalmaz.
            Az asztali darában csírarészecskék is előfordulhatnak, amelyek nagy tápértéket biztosítanak a terméknek.
        </p>

        <h3>Étkezési búzakorpa</h3>
        <p>
            Az étkezési búzakorpa a terméshéjat és a csíra egy részét tartalmazza, a lisztnél nagyobb szemcseméret-eloszlás jellemző rá.
            Ásványi anyag- és vitamintartalma magas, rostban gazdag, azonban rosttartalmának jelentős része nem emészthető.
            Ez azonban fontos a szervezet számára, mert jó hatással van az anyagcsere-folyamatokra, elősegíti a bélműködést,
            és csökkenti az éhségérzetet, így a fogyókúrás és diétás étrend kiegészítője is lehet.
        </p>

