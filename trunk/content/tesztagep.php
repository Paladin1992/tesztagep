<section>
    <div class="section-content">
        <p>
            A tésztagép 50-féle tészta elkészítésére alkalmas.
            Az alap tésztaadapterekkel 24 fajta tészta készíthető el, és a félkarú (szimpla) késsel 30-féle méretben darabolhatjuk a tésztát.
        </p>

        <p>
            A tésztagép 4-féle tarhonyát készít különböző méretekben:
        </p>
        <ul>
            <li>csigatésztát,</li>
            <li>kis kockát,</li>
            <li>széles metéltet,</li>
            <li>szarvacskát,</li>
            <li>gyufatésztát,</li>
            <li>csipetkét,</li>
            <li>2 fajta cérnametéltet (0,7 mm és 1 mm),</li>
            <li>3 különböző spagettit,</li>
            <li>kagylótésztát,</li>
            <li>fodros metéltet</li>
        </ul>

        <p>
            Az új tésztagép sárgarézfejek külön is meg vásárolhatók, egyedi kívánság szerint, amivel akár 50 fajta tészta is elkészíthető.
            A levehető fogaskerék megfordításával hosszan engedjük a tésztát,
            viszont ha a fogaskerekek összekapcsolt állapotban vannak, akkor a gép automatikusan apróra vág, egyenlő méretekre szeleteli a tésztát.
            A vágókést mindig úgy kell beállítani, hogy a kés belső fele közel legyen az adapterekhez (lehetőleg egy papírlapnyi távolságra).
            Vigyázni kell, hogy a kés a kiálló adapterek peremére ne ütközzön fel!
            A kést csak akkor kell felrakni, ha már nyomás alá helyeztük a gépet, vagyis feltöltöttük tésztával, mert a fej előre tolódik!
        </p>

        <p>
            Megjegyzés: A műanyag alkatrészek speciális anyagból, élelmiszeriparban használt teflonból készülnek, neve (pom anyag) hasonló,
            mint az ismert, erős danamid, csak élelmiszeriparban használt anyaga fehérszínű, csúszós, tapadásmentes, ezért mosni nem kell.
            Tésztatapadás kizárt! A súrlódása minimális, ezért sem melegíti fel a tésztát a gép hajtása közben.
            Lehetőleg vigyázzunk, hogy fémmel/tűvel ne tisztítsuk, mert sérti és karcolja a formát; használjunk inkább fából készült eszközt, pl. fogpiszkálót.
        </p>
    </div>
</section>

<section>
    <div class="section-content">
        <h2>Milyen lisztre van szükség?</h2>

        <img alt="Búza és liszt" src="images/buza-liszt.jpg" class="align-left padding-right"/>
        <!-- <img alt="Búza, liszt, zsák" src="images/buza-liszt-zsak.jpg"/> -->

        <p>
            Választhatunk <strong>rétesliszt</strong>et, <strong>finomliszt</strong>et, <strong>teljes kiőrlésű</strong>, vagy lisztérzékenyeknek <strong>gluténmentes</strong> lisztet.
        </p>

        <p>
            Lehetőleg réteslisztet használjunk, mert jobb a sikértartalma, és nem ragad úgy, mint a finomliszt,
            vagy teljes kiőrlésű lisztet, mivel az köztudottan nagyon egészséges.
        </p>

        <p>
            A finomlisztnek alacsony a sikér tartalma, és általában beszürkül a tészta, ezért célszerű lehet rétesliszttel elkeverni fele-fele arányban.
        </p>
    </div>
</section>

<section>
    <div class="section-content">
        <h2>Milyen tésztára van szükség?</h2>

        <img alt="Tészta" src="images/teszta.jpg" class="align-right padding-left"/>

        <p>
            <strong>A tésztát kigyúrni nem kell</strong>, csupán össze kell állítani úgy, mint a reszelt tésztát, csak annál kicsit lágyabbra.
            Kezdő tésztagéphasználók lehetőleg gyúrják ki gyurma állagúra, és pihentessék meg a tésztát.
            A tészta akkor jó, ha nem tapad kézhez és képlékeny marad, ezáltal nem fog ragadni.
        </p>

        <p>
            1 kg tészta elkészítéséhez 30-40 dkg liszt és (nagyságtól függően) 9-10 db tojás szükséges.
        </p>

        <p>
            A tésztát a gépben lévő tefloncsiga simára gyúrja, és a gép használata során nem melegedik be a tészta.
            (Ugyanis a melegedéstől a tészta belágyul és ragad.)
        </p>
    </div>
</section>

